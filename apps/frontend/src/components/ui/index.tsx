interface StatsCardProps {
  title: string
  value: string | number
  change?: { value: number; label: string }
  icon: React.ReactNode
  color?: 'blue' | 'green' | 'orange' | 'red' | 'purple'
}

export function StatsCard({ title, value, change, icon, color = 'blue' }: StatsCardProps) {
  const colorMap = {
    blue: 'bg-blue-50 text-[#1e3a5f]',
    green: 'bg-green-50 text-green-700',
    orange: 'bg-orange-50 text-orange-700',
    red: 'bg-red-50 text-red-700',
    purple: 'bg-purple-50 text-purple-700',
  }

  return (
    <Card>
      <CardContent className="p-6">
        <div className="flex items-center justify-between mb-4">
          <p className="text-sm font-medium text-gray-500">{title}</p>
          <div className={cn('rounded-lg p-2', colorMap[color])}>{icon}</div>
        </div>
        <div className="flex items-end justify-between">
          <p className="text-3xl font-bold text-gray-900">{value}</p>
          {change && (
            <span
              className={cn(
                'text-xs font-medium flex items-center gap-1',
                change.value >= 0 ? 'text-green-600' : 'text-red-600',
              )}
            >
              {change.value >= 0 ? '↑' : '↓'} {Math.abs(change.value)}% {change.label}
            </span>
          )}
        </div>
      </CardContent>
    </Card>
  )
}
